// ClosestColourName returns the closest XKCD color name for the given RGB (0-255)
func ClosestColourName(r, g, b uint8) string {
	minDist := int64(1 << 62)
	name := ""

	for _, c := range colourNames {
		dr := int64(r) - int64(c.R)
		dg := int64(g) - int64(c.G)
		db := int64(b) - int64(c.B)

		dist := dr*dr + dg*dg + db*db

		if dist < minDist {
			minDist = dist
			name = c.Name
		}
	}

	return name
}
